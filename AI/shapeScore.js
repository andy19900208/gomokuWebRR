export default function (shape) {
  switch (shape) {

    case 41:
      return 10000;
    case -41:
      return 10000;

    case 32:
      return 1000;
    case -32:
      return 1000;

    case 31:
      return 100;
    case -31:
      return 100;

    case 22:
      return 100;
    case -22:
      return 100;

    case 21:
      return 10;
    case -21:
      return 10;

    case 12:
      return 10;
    case -12:
      return 10;

    case 11:
      return 1;
    case -11:
      return 1;

    
    
    default :
      return 0;

  }


}